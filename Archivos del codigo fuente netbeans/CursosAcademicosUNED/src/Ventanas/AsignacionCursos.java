    ///
    ///carga el estudiante actual desde la sesión y lo muestra en el campo txtEstudiante
    ///
private void cargarEstudianteActual() {
    Estudiante estudiante = SesionEstudiante.obtenerEstudiante();


    if (estudiante != null) {
        //se muestra el ID y el nombre completo en el campo de solo lectura
        String texto = estudiante.getId() + " - " + estudiante.getNombre();
        txtEstudiante.setText(texto);
        txtEstudiante.setEditable(false);
    } else {
        txtEstudiante.setText("No hay sesión activa");
    }
}

///
///carga los cursos activos desde archivo y los agrega al combo cmbCurso
///
private void cargarCursosActivos() {
    cmbCurso.removeAllItems(); //limpia el combo primero
    CursoLN cursoLN = new CursoLN();
    List<Curso> cursos = cursoLN.listarActivos();


    for (Curso curso : cursos) {
        cmbCurso.addItem(curso); //usa toString para mostrarlo bien
    }
}


