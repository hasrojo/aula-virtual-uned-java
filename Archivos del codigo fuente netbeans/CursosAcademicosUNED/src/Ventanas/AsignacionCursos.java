    private void btnAsignarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarCursoActionPerformed
         // Obtener estudiante actual desde la sesión
    Estudiante estudiante = SesionEstudiante.obtenerEstudiante();
    if (estudiante == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "No hay estudiante autenticado.");
        return;
    }

    //obtener curso seleccionado
    Curso cursoSeleccionado = (Curso) cmbCurso.getSelectedItem();
    if (cursoSeleccionado == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar un curso.");
        return;
    }

    //llama a ArchivoAsignacion para guardar
    String resultado = AccesoADatos.ArchivoAsignacion.asignarCurso(
        estudiante.getId(), cursoSeleccionado.getNombre()
    );

    //mostrar resultado
    javax.swing.JOptionPane.showMessageDialog(this, resultado);
    }//GEN-LAST:event_btnAsignarCursoActionPerformed

