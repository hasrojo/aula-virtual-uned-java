    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        ///
        ///busca al estudiante por cédula y muestra sus datos y cursos asignados
        ///
        
         String cedula = txtCedula.getText().trim();

    if (cedula.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar o seleccionar una cédula.");
        return;
    }

    try {
        Estudiante est = AccesoADatos.ArchivoEstudiante.cargarEstudiantePorId(cedula);
        if (est == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Estudiante no encontrado.");
            return;
        }

        mostrarDatosEstudiante(est);
        mostrarCursosAsignados(cedula);

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

