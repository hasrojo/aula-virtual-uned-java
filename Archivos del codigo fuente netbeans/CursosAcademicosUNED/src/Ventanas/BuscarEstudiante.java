    ///
    ///llena el combo con todos los estudiantes registrados (ID + nombre completo)
    ///
    private void llenarComboEstudiantes() {
    cmbEstudiantes.removeAllItems(); //limpia el combo

    //agregar opción inicial
    cmbEstudiantes.addItem("Seleccione uno");

    File carpeta = new File("registros_estudiantes");
    if (!carpeta.exists()) {
        return;
    }

    File[] archivos = carpeta.listFiles();
    if (archivos == null) {
        return;
    }

    for (File archivo : archivos) {
        try {
            String id = archivo.getName().replace(".txt", "");
            Estudiante est = AccesoADatos.ArchivoEstudiante.cargarEstudiantePorId(id);
            if (est != null) {
                cmbEstudiantes.addItem(id + " - " + est.getNombre() + " " + est.getPrimerApellido());
            }
        } catch (Exception e) {
            
        }
    }

    // Dejar seleccionado el "Seleccione uno"
    cmbEstudiantes.setSelectedIndex(0);
}





///
///cando se selecciona un estudiante en el combo se llena el campo de cédula
///
private void cmbEstudiantesActionPerformed(java.awt.event.ActionEvent evt) {
    String seleccionado = (String) cmbEstudiantes.getSelectedItem();
    if (seleccionado != null && seleccionado.contains(" - ")) {
        String id = seleccionado.split(" - ")[0];
        txtCedula.setText(id);
    }
}





    ///
    ///muestra los datos del estudiante en tablaEstudiante (una sola fila).
    ///
private void mostrarDatosEstudiante(Estudiante est) {
    String[] columnas = {"ID", "Nombre", "Apellido 1", "Apellido 2", "Nacimiento", "Email", "Teléfono"};
    Object[][] datos = {
        {
            est.getId(),
            est.getNombre(),
            est.getPrimerApellido(),
            est.getSegundoApellido(),
            est.getFechaNacimiento(),
            est.getEmail(),
            est.getTelefono()
        }
    };

    tablaEstudiante.setModel(new javax.swing.table.DefaultTableModel(datos, columnas));
}







///
///mestra los cursos asignados al estudiante en tablaCursos
///
private void mostrarCursosAsignados(String cedula) {
    List<String> cursos = AccesoADatos.ArchivoAsignacion.obtenerCursosAsignados(cedula);

    String[] columnas = {"Cursos asignados"};
    Object[][] datos = new Object[cursos.size()][1];

    for (int i = 0; i < cursos.size(); i++) {
        datos[i][0] = cursos.get(i);
    }

    tablaCursos.setModel(new javax.swing.table.DefaultTableModel(datos, columnas));
}







///
/// Toma el ítem seleccionado del combo y coloca la cédula en txtCedula
/// Acepta tanto "ID - Nombre Apellido" (String) como objeto Estudiante
///
private void actualizarCedulaDesdeCombo() {
    Object sel = cmbEstudiantes.getSelectedItem();
    if (sel == null) {
        return;
    }

    String seleccionado = sel.toString();
    if (seleccionado.equals("Seleccione uno")) {
        txtCedula.setText("");
        return;
    }

    int guion = seleccionado.indexOf(" - ");
    if (guion > 0) {
        txtCedula.setText(seleccionado.substring(0, guion).trim());
    }
}



  

    ///
    ///configura las columnas de las tablas para que se vean aunque no haya datos
    ///
private void inicializarTablas() {
    // tablaEstudiante: una sola fila cuando haya datos
    javax.swing.table.DefaultTableModel modeloEst = new javax.swing.table.DefaultTableModel(
        new Object[][]{}, 
        new String[]{"ID", "Nombre", "Apellido 1", "Apellido 2", "Nacimiento", "Email", "Teléfono"}
    ) {
        public boolean isCellEditable(int row, int col) { return false; }
    };
    tablaEstudiante.setModel(modeloEst);

    // tablaCursos: listado de cursos asignados
    javax.swing.table.DefaultTableModel modeloCursos = new javax.swing.table.DefaultTableModel(
        new Object[][]{}, 
        new String[]{"Cursos asignados"}
    ) {
        public boolean isCellEditable(int row, int col) { return false; }
    };
    tablaCursos.setModel(modeloCursos);

    // Opcional: bloquear mover columnas
    tablaEstudiante.getTableHeader().setReorderingAllowed(false);
    tablaCursos.getTableHeader().setReorderingAllowed(false);
}

    
    

